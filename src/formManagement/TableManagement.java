     public void showData() {
        try {
//clear data
            int totalRow = tbTable.getRowCount() - 1;
            while (totalRow > - 1) {
                modelTable.removeRow(totalRow);
                totalRow--;
            }
// add record
            String search = txtSearchTable.getText();
            String sql = " SELECT * FROM tb_table "
                    + " where "
                    + " table_id LIKE ('%" + search + "%' )"
                    + " OR table_status LIKE ('%" + search + "%')";
            ResultSet rs = conn.createStatement().executeQuery(sql);
            int row = 0;
            while (rs.next()) {
                modelTable.addRow(new Object[0]);
                modelTable.setValueAt(rs.getString("table_id"), row, 0);
                modelTable.setValueAt(rs.getString("table_status"), row, 1);
                row++;
            }
            tbTable.setModel(modelTable);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    

